package declaraciondevariable;


public class Intro_Java_ManosDeObra3 {

    
    public static void main(String[] args) {
       
        double numero1=25;
        double numero2=30;
        
        double suma;
        double dividir;
        
        System.out.println("El operador es suma. El total es.:");
        suma=numero1+numero2;
        System.out.println(suma);
        System.out.println("El operador es dividir. El total es.:");
        dividir=numero1/numero2;
        System.out.println(dividir);
    }
    
}
